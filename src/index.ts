export function sum(a: number, b: number, c: number): number {
  return a + b + c;
}

export function multiply(a: number, b: number): number {
  return a * b;
}

export function power(a: number, b: number): number {
  return Math.pow(a, b);
}

export function mod(a: number, b: number): number {
  return a % b;
}
